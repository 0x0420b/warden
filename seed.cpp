template <size_t N> 
void Print(std::array<uint8_t, N> const& seed)
{
    std::cout << "{";
    for (size_t i = 0; i < seed.size(); ++i)
        std::cout << ' ' << std::setfill('0') << std::setw(2) << std::hex << uint32_t(seed[i]);
    std::cout << " }";
}

std::array<uint8_t, 16> TransformSeed(std::array<uint8_t, 16> const& seed);

int main()
{
    std::array<uint8_t, 16> seed = { 0x4D, 0x80, 0x8D, 0x2C, 0x77, 0xD9, 0x05, 0xC4, 0x1A, 0x63, 0x80, 0xEC, 0x08, 0x58, 0x6A, 0xFE };

    std::cout << "Initial seed:     ";
    Print(seed);
    std::cout << std::endl;

    std::cout << "First transform:  ";
    auto clientSeed = TransformSeed(seed);
    Print(clientSeed);
    std::cout << std::endl;

    std::cout << "Second transform: ";
    auto serverSeed = TransformSeed(clientSeed);
    Print(serverSeed);
    std::cout << std::endl;

    auto getDigest = [](std::array<uint8_t, 16> const& seed) {

        std::array<uint8_t, SHA_DIGEST_LENGTH> digest;

        SHA_CTX ctx;
        SHA1_Init(&ctx);
        SHA1_Update(&ctx, seed.data(), seed.size());
        SHA1_Final(digest.data(), &ctx);

        return digest;
    };

    auto clientDigest = getDigest(clientSeed);
    auto serverDigest = getDigest(serverSeed);

    std::cout << "First digest:  ";
    Print(clientDigest);
    std::cout << std::endl;

    std::cout << "Second digest: ";
    Print(serverDigest);
    std::cout << std::endl;

    return 0;
}

